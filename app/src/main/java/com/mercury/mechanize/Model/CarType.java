package com.mercury.mechanize.Model;

public class CarType {
    private String make;
    private String model;

    public CarType() {
    }

    public String getMake() {
        return make;
    }

    public void setMake(String make) {
        this.make = make;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }
}
